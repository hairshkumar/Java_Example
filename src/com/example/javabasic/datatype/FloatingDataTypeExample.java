package com.example.javabasic.datatype;

public class FloatingDataTypeExample {

	public static void main(String[] args) {
		double doublenum = 19.97d;
    	float floatnum = 19.98f;
    	System.out.println("Float value = "+ floatnum);
    	System.out.println("Double Value = "+doublenum);    	
    	System.out.println("let See precision of float point");
    	doublenum=19.123456789d;
    	floatnum=19.123456789f;
    	System.out.println("Float value = "+ floatnum);
    	System.out.println("Double Value = "+doublenum);
	}
}