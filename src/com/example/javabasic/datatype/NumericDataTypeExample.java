package com.example.javabasic.datatype;

public class NumericDataTypeExample {

	public static void main(String[] args) {
		byte b=120;
		short s=12000;
		int i=120000000;
		long l=120000000000000000l; 
		System.out.println("value of b = "+b);
		System.out.println("values of short = "+s);
		System.out.println("values of int = "+i);
		System.out.println("values of long = "+l);
	}
}