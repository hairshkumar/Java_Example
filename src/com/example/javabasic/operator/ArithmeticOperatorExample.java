package com.example.javabasic.operator;

public class ArithmeticOperatorExample {

	public static void main(String[] args) {
		int a = 5, b = 10;
		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		System.out.println("a % b = " + (a % b));
	}
}