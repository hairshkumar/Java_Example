package com.example.javabasic.operator;

public class RelationalOpEx {

	public static void main(String[] args) {
		int a = 5, b = 10;
		System.out.println("5 == 10 " + (a == b));
		System.out.println("5 != 10 " + (a != b));
		System.out.println("5 > 10 " + (a > b));
		System.out.println("5 < 10 " + (a < b));
		System.out.println("5 >= 10 " + (a >= b));
		System.out.println("5 <= 10 " + (a <= b));
	}
}